-- StudyAbroadOS Supabase schema
-- Run this file in Supabase Dashboard > SQL Editor.
-- It stores all business data in Postgres and isolates rows by auth.uid().

create or replace function public.set_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

create table if not exists public.profiles (
  id bigint generated by default as identity primary key,
  user_id uuid not null unique references auth.users(id) on delete cascade,
  target_intake text,
  name text,
  undergraduate_major text,
  grade text,
  current_gpa numeric default 0,
  gpa_scale numeric default 4,
  toefl_current integer default 0,
  toefl_target integer default 0,
  gre_gmat_status text,
  target_regions text,
  target_majors text,
  budget_range text,
  internships text,
  research_experience text,
  competitions_projects text,
  tutoring_part_time text,
  stock_investment text,
  career_goals text,
  notes text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.programs (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  region text,
  school_name text not null,
  program_name text not null,
  degree_type text,
  academic_direction text,
  website text,
  source_url text,
  verified_date date,
  is_shared boolean not null default false,
  tuition numeric default 0,
  living_cost numeric default 0,
  application_fee numeric default 0,
  deadline date,
  gpa_requirement text,
  toefl_requirement text,
  gre_gmat_requirement text,
  recommendation_requirement text,
  essay_requirement text,
  scholarship_available boolean default false,
  employment_notes text,
  category text,
  status text,
  notes text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

alter table public.programs add column if not exists source_url text;
alter table public.programs add column if not exists verified_date date;
alter table public.programs add column if not exists is_shared boolean not null default false;

create table if not exists public.application_tasks (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  task_name text not null,
  related_program text,
  task_type text,
  deadline date not null,
  priority text,
  status text,
  notes text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.writing_materials (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  experience_name text not null,
  experience_type text,
  period text,
  background text,
  action text,
  result text,
  abilities text,
  usage text,
  notes text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.budgets (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  program_ref text not null,
  application_fee numeric default 0,
  score_report_fee numeric default 0,
  tuition numeric default 0,
  living_cost numeric default 0,
  visa_flight_estimate numeric default 0,
  scholarship_amount numeric default 0,
  total_cost numeric default 0,
  notes text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

-- Optional tutor/workbench tables for student tutoring scenarios.
create table if not exists public.students (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  name text not null,
  grade text,
  current_score numeric,
  target_score numeric,
  school_region text,
  current_chapter text,
  main_problems text,
  parent_concerns text,
  notes text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.courses (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  student_id bigint references public.students(id) on delete cascade,
  class_date date not null,
  duration numeric default 2,
  topic text,
  performance text,
  completion text,
  next_focus text,
  feedback_sent boolean default false,
  prepared boolean default false,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.mistakes (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  student_id bigint references public.students(id) on delete cascade,
  mistake_date date not null,
  chapter text,
  description text,
  reason text,
  knowledge_point text,
  repeated boolean default false,
  next_suggestion text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.feedbacks (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  student_id bigint references public.students(id) on delete cascade,
  course_id bigint references public.courses(id) on delete set null,
  feedback_date date not null,
  content text not null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.incomes (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  student_id bigint references public.students(id) on delete cascade,
  class_date date not null,
  hours numeric not null,
  unit_price numeric not null,
  amount numeric not null,
  paid boolean default false,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists idx_profiles_user_id on public.profiles(user_id);
