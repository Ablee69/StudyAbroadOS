create index if not exists idx_materials_user_id on public.writing_materials(user_id);
create index if not exists idx_budgets_user_id on public.budgets(user_id);
create index if not exists idx_students_user_id on public.students(user_id);
create index if not exists idx_courses_user_id_date on public.courses(user_id, class_date);
create index if not exists idx_mistakes_user_id_date on public.mistakes(user_id, mistake_date);
create index if not exists idx_feedbacks_user_id_date on public.feedbacks(user_id, feedback_date);
create index if not exists idx_incomes_user_id_date on public.incomes(user_id, class_date);

